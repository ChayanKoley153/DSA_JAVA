
public class case_switch {
    public static void main(String args[]) {

        char ch = 'a';
        
        switch (ch){
            case 'd' : System.out.println("samosa");
                    break;
            case 'a' : System.out.println("burger");
                    break;
            case 'c' : System.out.println("mango shake");
                    break;
            default : System.out.println("we wake up");
        }
        
    }
    
}